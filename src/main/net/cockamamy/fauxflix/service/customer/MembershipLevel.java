package net.cockamamy.fauxflix.service.customer;

/**
 * 
 * Defines the Faux Flix rental store customer membership levels
 * 
 * @author jburwell
 * 
 * @since 1.0.0
 * 
 */
public enum MembershipLevel {

	/**
	 * 
	 * The standard membership level which permits up to two (2) rentals of any
	 * media type at any given time.
	 * 
	 * @since 1.0.0
	 * 
	 */
	STANDARD,

	/**
	 * 
	 * The executive membership level which permits up to three (3) DVD and two
	 * (2) Bluray rentals at any given time.
	 * 
	 * @since 1.0.0
	 * 
	 */
	EXECUTIVE;

}
