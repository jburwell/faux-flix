package net.cockamamy.fauxflix.service.inventory;

/**
 * 
 * The types of media Faux Flix rental store rents
 * 
 * @author jburwell
 * 
 * @since 1.0.0
 * 
 */
public enum MediaType {

	/**
	 * 
	 * Standard DVD media type
	 * 
	 * @since 1.0.0
	 * 
	 */
	DVD,

	/**
	 * 
	 * Blueray media type
	 * 
	 * @since 1.0.0
	 * 
	 */
	BLURAY,

	/**
	 * 
	 * Logically null media type
	 * 
	 * @since 1.0.0
	 * 
	 */
	NONE;

}
